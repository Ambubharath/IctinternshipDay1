console.log("CONTROL STATEMENTS");
//if statement
let a=10;
let b=20;
if(!(b<a)){
    console.log("b is greater than a")
}
else{
    console.log("a is greater than b");
}
//for statement
for(let i=1;i<=10;i++){
    console.log(i);
}
//while statement
let i=1
while(i<=10){
    console.log(i);
}
// do while statement
let i=1
do{
    console.log(i);
    i++;
}while(i<=10)